export type GoalKind =
  | "understand"
  | "agree"
  | "boundary"
  | "announce"
  | "support"
  | "trust"
  | "other";

export type ReactionPair = { child: string; parent: string };

export type PlanStep = {
  title: string;
  why: string;
  action: string;
  phrase?: string;
  questions?: string[];
  reactions?: ReactionPair[];
  avoid?: string;
  /** «К чему прийти» — optional outcome of the step */
  outcome?: string;
};

export type ConversationPlan = {
  title: string;
  reminder: string;
  steps: PlanStep[];
  nonNegotiable?: string;
  discussable?: string;
};

export const goalOptions: { id: GoalKind; label: string }[] = [
  { id: "understand", label: "Понять, почему ребёнок так поступил" },
  { id: "agree", label: "Договориться о правиле или изменении" },
  { id: "boundary", label: "Обозначить границу" },
  { id: "announce", label: "Сообщить ребёнку важное решение" },
  { id: "support", label: "Поддержать ребёнка" },
  { id: "trust", label: "Восстановить доверие после обмана или конфликта" },
  { id: "other", label: "Другое" },
];

export const goalLabel = (kind: GoalKind) =>
  goalOptions.find((g) => g.id === kind)?.label ?? "Другое";

/** Heuristic suggestion from situation text — parent must confirm. */
export function suggestGoalKind(situation: string, topic: string): GoalKind {
  const text = `${situation} ${topic}`.toLowerCase();

  if (
    /обман|соврал|скрыл|сказал.{0,20}что|не сказал|лож|довери|двойк|тройк|оценк/.test(
      text,
    )
  ) {
    if (/двойк|тройк|оценк|соврал|скрыл|обман|лож/.test(text)) return "understand";
    return "trust";
  }
  if (/поддержать|грустит|расстроен|страх|тревож|конфликт с друзь/.test(text)) {
    return "support";
  }
  if (/решил|переезд|смен.{0,8}школ|сообщ|объяв/.test(text)) return "announce";
  if (/запрет|нельзя|границ|обязательн/.test(text)) return "boundary";
  if (
    /телефон|гаджет|интернет|допоздн|договор|правило|время|опозда/.test(text)
  ) {
    return "agree";
  }
  return "other";
}

type BuildCtx = {
  topic: string;
  age: string;
  situation: string;
  goalText: string;
  reaction: string;
};

function topicTitle(ctx: BuildCtx, fallback: string) {
  const text = `${ctx.situation} ${ctx.goalText} ${ctx.topic}`.toLowerCase();
  if (/телефон|гаджет/.test(text)) return "Телефон перед сном";
  if (/двойк|тройк|оценк/.test(text)) return "Скрытая оценка";
  if (/обязан|уборк|дом/.test(text)) return "Домашние обязанности";
  if (/компьютер|игр/.test(text)) return "Компьютер по ночам";
  if (/школ/.test(text)) return "Смена школы";
  if (/друг|друз/.test(text)) return "Конфликт с друзьями";
  if (ctx.topic && ctx.topic !== "Другое") return ctx.topic;
  return fallback;
}

function isPhoneScenario(ctx: BuildCtx) {
  return /телефон|гаджет/i.test(`${ctx.topic} ${ctx.situation} ${ctx.goalText}`);
}

function isGradeLieScenario(ctx: BuildCtx) {
  return /двойк|тройк|оценк|скрыл|соврал/i.test(
    `${ctx.situation} ${ctx.goalText}`,
  );
}

