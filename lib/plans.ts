export type GoalKind =
  | "understand"
  | "agree"
  | "boundary"
  | "announce"
  | "support"
  | "trust"
  | "other";

export type ReactionPair = {
  /** Short condition after «Если ребёнок…», e.g. «молчит», «спорит» */
  when?: string;
  child: string;
  parent: string;
};

export type PlanStep = {
  title: string;
  why: string;
  /** Short how-to for the parent (shown as lead text) */
  action: string;
  /** Legacy single phrase — prefer phrases[] */
  phrase?: string;
  /** «Можно сказать» — one or more parent lines */
  phrases?: string[];
  /** «Можно спросить» */
  questions?: string[];
  /** «Если ребёнок…» + «Можно ответить» */
  reactions?: ReactionPair[];
  /** «Важно обозначить» — rule, decision, or boundary */
  mark?: string;
  /** «Обсудите вместе» — details, options, next step */
  discuss?: string;
  /** «Обратите внимание» — orientation for the parent */
  note?: string;
  /** «Лучше избегать» */
  avoid?: string;
  /** Optional outcome of the step */
  outcome?: string;
};

export type ConversationPlan = {
  title: string;
  /** Concrete goal for this situation — not the base goal label */
  goal?: string;
  reminder: string;
  steps: PlanStep[];
  nonNegotiable?: string;
  discussable?: string;
};

export const goalOptions: { id: GoalKind; label: string }[] = [
  { id: "understand", label: "Понять, почему ребёнок так поступил" },
  { id: "agree", label: "Договориться о правиле или изменении" },
  { id: "boundary", label: "Установить границу" },
  { id: "announce", label: "Сообщить ребёнку важное решение" },
  { id: "support", label: "Поддержать ребёнка" },
  { id: "trust", label: "Восстановить доверие после обмана или конфликта" },
  { id: "other", label: "Другое" },
];

/** Collect sayable lines from a step (phrases[] or legacy phrase). */
export function stepPhrases(step: PlanStep): string[] {
  const fromList = (step.phrases || []).map((p) => p.trim()).filter(Boolean);
  if (fromList.length) return fromList;
  const one = step.phrase?.trim();
  return one ? [one] : [];
}

/** Short «Если ребёнок …» label from the form's typical reaction. */
export function reactionWhenLabel(reaction: string): string {
  const r = reaction.trim().toLowerCase();
  if (!r) return "";
  if (/молч|замкн/.test(r)) return "молчит";
  if (/спор|защищ/.test(r)) return "спорит";
  if (/злит/.test(r)) return "злится";
  if (/обвиня/.test(r)) return "обвиняет";
  if (/расстра/.test(r)) return "расстраивается";
  if (/соглаша/.test(r)) return "соглашается, но не делает";
  return reaction.trim().replace(/^если\s+ребёнок\s+/i, "");
}

export const goalLabel = (kind: GoalKind) =>
  goalOptions.find((g) => g.id === kind)?.label ?? "Другое";

/** True when the field is empty or just repeats a base goal option. */
export function isGenericGoalText(text: string): boolean {
  const t = text.trim();
  if (!t) return true;
  return goalOptions.some((g) => g.label.toLowerCase() === t.toLowerCase());
}

/**
 * Formulate a concrete goal from the situation + base goal kind
 * when the parent did not write their own result.
 */
export function deriveSituationGoal(
  situation: string,
  kind: GoalKind,
  goalText = "",
): string {
  if (goalText.trim() && !isGenericGoalText(goalText)) {
    return goalText.trim().replace(/\s+/g, " ");
  }

  const s = situation.toLowerCase();
  const phone = /телефон|гаджет|экран|комп|игр/.test(s);
  const night = /ноч|допоздн|утра|спать|сн|отбой|22|23/.test(s);
  const grade = /двойк|тройк|оценк/.test(s);
  const lied = /скрыл|соврал|обман|не сказал|лож/.test(s);
  const chores = /обязан|уборк|дом/.test(s);
  const late = /опозда/.test(s);
  const friends = /друг|друз/.test(s);
  const school = /школ/.test(s);

  switch (kind) {
    case "boundary":
      if (phone && night) return "Обозначить границу: без гаджетов на ночь";
      if (phone) return "Обозначить границу по использованию гаджетов";
      if (chores) return "Обозначить границу по домашним обязанностям";
      if (late) return "Обозначить границу: без опозданий";
      return "Обозначить вашу границу в этой ситуации";
    case "agree":
      if (phone && night) return "Договориться о правиле: гаджеты убираем до сна";
      if (phone) return "Договориться о правиле по экранному времени";
      if (chores) return "Договориться о правиле по домашним делам";
      if (late) return "Договориться, как убрать опоздания";
      return "Договориться о понятном правиле на эту ситуацию";
    case "understand":
      if (grade && lied) return "Понять, почему ребёнок скрыл оценку";
      if (lied) return "Понять, почему ребёнок не сказал правду";
      if (phone) return "Понять, почему гаджеты важнее режима";
      return "Понять, почему ребёнок так поступил в этой ситуации";
    case "announce":
      if (school) return "Сообщить решение о школе спокойно и ясно";
      return "Сообщить принятое решение и объяснить, что дальше";
    case "support":
      if (friends) return "Поддержать ребёнка в ситуации с друзьями";
      return "Поддержать ребёнка и дать пространство рассказать";
    case "trust":
      if (grade || lied) return "Восстановить доверие после сокрытия правды";
      return "Восстановить доверие и договориться говорить честно";
    default:
      if (phone && night) return "Разобрать ночное использование гаджетов";
      return "Прийти к ясному следующему шагу по этой ситуации";
  }
}

/** Heuristic suggestion from situation text — parent must confirm. */
export function suggestGoalKind(situation: string, topic: string): GoalKind {
  const text = `${situation} ${topic}`.toLowerCase();

  if (
    /обман|соврал|скрыл|сказал.{0,20}что|не сказал|лож|довери|двойк|тройк|оценк/.test(
      text,
    )
  ) {
    if (/двойк|тройк|оценк|соврал|скрыл|обман|лож/.test(text)) return "understand";
    return "trust";
  }
  if (/поддержать|грустит|расстроен|страх|тревож|конфликт с друзь/.test(text)) {
    return "support";
  }
  if (/решил|переезд|смен.{0,8}школ|сообщ|объяв/.test(text)) return "announce";
  if (/запрет|нельзя|границ|обязательн/.test(text)) return "boundary";
  if (
    /телефон|гаджет|интернет|допоздн|договор|правило|время|опозда/.test(text)
  ) {
    return "agree";
  }
  return "other";
}

type BuildCtx = {
  topic: string;
  age: string;
  situation: string;
  goalText: string;
  reaction: string;
};

function topicTitle(ctx: BuildCtx, fallback: string) {
  const text = `${ctx.situation} ${ctx.goalText}`.toLowerCase();
  if (/телефон/.test(text) && /(ноч|допоздн|утра|спать)/.test(text)) {
    return "Телефон по ночам";
  }
  if (/(комп|компьютер|игр)/.test(text) && /(ноч|допоздн|утра)/.test(text)) {
    return "Ночи за компьютером";
  }
  if (/телефон|гаджет/.test(text)) return "Телефон и экранное время";
  if (/двойк|тройк|оценк/.test(text)) return "Скрытая оценка";
  if (/обязан|уборк/.test(text)) return "Домашние обязанности";
  if (/опозда/.test(text)) return "Опоздания";
  if (/друг|друз/.test(text)) return "Конфликт с друзьями";
  if (/школ/.test(text)) return "Смена школы";

  const first = ctx.situation.trim().split(/[.!?\n]/)[0]?.trim() || "";
  if (first && first.length >= 8 && first.toLowerCase() !== ctx.topic.toLowerCase()) {
    const short =
      first.length > 52
        ? `${first.slice(0, 49).replace(/\s+\S*$/, "")}…`
        : first;
    return short.charAt(0).toUpperCase() + short.slice(1);
  }
  return fallback;
}

function isPhoneScenario(ctx: BuildCtx) {
  return /телефон|гаджет/i.test(`${ctx.topic} ${ctx.situation} ${ctx.goalText}`);
}

function isGradeLieScenario(ctx: BuildCtx) {
  return /двойк|тройк|оценк|скрыл|соврал/i.test(
    `${ctx.situation} ${ctx.goalText}`,
  );
}

